/**
 * Render a 2D pixel array sprite to a canvas context.
 * @param {CanvasRenderingContext2D} ctx - Canvas context
 * @param {number[][]} spriteData - 2D array of palette indices
 * @param {string[]} palette - Array of color strings (index 0 = transparent)
 * @param {number} x - X position on canvas
 * @param {number} y - Y position on canvas
 * @param {number} [scale=2] - Pixel scale factor
 * @param {boolean} [flipH=false] - Horizontal flip
 */
function renderSprite(ctx, spriteData, palette, x, y, scale, flipH) {
  if (scale === undefined) scale = 2;
  if (flipH === undefined) flipH = false;

  const h = spriteData.length;
  const w = spriteData[0].length;

  ctx.save();

  if (flipH) {
    ctx.translate(x + w * scale, y);
    ctx.scale(-1, 1);
    x = 0;
    y = 0;
  }

  for (let row = 0; row < h; row++) {
    for (let col = 0; col < w; col++) {
      const idx = spriteData[row][col];
      if (idx === 0) continue; // transparent
      const color = palette[idx];
      if (!color || color === 'transparent') continue;
      ctx.fillStyle = color;
      ctx.fillRect(x + col * scale, y + row * scale, scale, scale);
    }
  }

  ctx.restore();
}

/**
 * Render an 8x8 tile to a canvas context.
 * @param {CanvasRenderingContext2D} ctx - Canvas context
 * @param {number[][]} tileData - 8x8 array of palette indices
 * @param {string[]} palette - Array of color strings
 * @param {number} x - X position
 * @param {number} y - Y position
 * @param {number} [scale=2] - Pixel scale factor
 */
function renderTile(ctx, tileData, palette, x, y, scale) {
  if (scale === undefined) scale = 2;
  for (let row = 0; row < tileData.length; row++) {
    for (let col = 0; col < tileData[row].length; col++) {
      const idx = tileData[row][col];
      if (idx === 0) continue;
      const color = palette[idx];
      if (!color || color === 'transparent') continue;
      ctx.fillStyle = color;
      ctx.fillRect(x + col * scale, y + row * scale, scale, scale);
    }
  }
}

/**
 * Fill a rectangular area with repeating tiles.
 * @param {CanvasRenderingContext2D} ctx
 * @param {number[][]} tileData - 8x8 tile
 * @param {string[]} palette
 * @param {number} x - Start X
 * @param {number} y - Start Y
 * @param {number} width - Area width in pixels
 * @param {number} height - Area height in pixels
 * @param {number} [scale=2]
 */
function fillTiles(ctx, tileData, palette, x, y, width, height, scale) {
  if (scale === undefined) scale = 2;
  const tw = tileData[0].length * scale;
  const th = tileData.length * scale;
  for (let ty = y; ty < y + height; ty += th) {
    for (let tx = x; tx < x + width; tx += tw) {
      renderTile(ctx, tileData, palette, tx, ty, scale);
    }
  }
}

/**
 * Get animation frame for a given time.
 * @param {number[][]} frames - Array of sprite frames
 * @param {number} time - Current time in ms
 * @param {number} [fps=4] - Animation speed
 * @returns {number[][]} The current frame
 */
function getAnimFrame(frames, time, fps) {
  if (fps === undefined) fps = 4;
  if (!frames || frames.length === 0) return null;
  const idx = Math.floor(time / (1000 / fps)) % frames.length;
  return frames[idx];
}
