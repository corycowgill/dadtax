// ---- HELPER: Create a 2D array filled with 0 ----
function makeGrid(w, h) {
  const g = [];
  for (let r = 0; r < h; r++) {
    g.push(new Array(w).fill(0));
  }
  return g;
}

// ---- HELPER: Paint rows from compact string data ----
// Each character is a hex digit (0-f) representing a palette index
function paintFromHex(grid, startRow, hexRows) {
  for (let i = 0; i < hexRows.length; i++) {
    const row = startRow + i;
    if (row >= grid.length) break;
    const hex = hexRows[i];
    for (let c = 0; c < hex.length && c < grid[0].length; c++) {
      grid[row][c] = parseInt(hex[c], 16);
    }
  }
  return grid;
}

